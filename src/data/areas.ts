// ── 헬퍼 함수 ──

/** slug로 지역 정보 찾기 */
export const getAreaBySlug = (slug: string): AreaInfo | undefined =>
  AREAS.find(a => a.slug === slug);

/** 구별 그룹화 */
export const getAreasByGu = (): Record<string, AreaInfo[]> => {
  const groups: Record<string, AreaInfo[]> = {};
  AREAS.forEach(a => {
    if (!groups[a.gu]) groups[a.gu] = [];
    groups[a.gu].push(a);
  });
  return groups;
};

/** 거리순 정렬 */
export const getAreasSorted = (): AreaInfo[] =>
  [...AREAS].sort((a, b) => a.distKm - b.distKm);

/** 전체 키워드 목록 (메타 태그용) */
export const getAllAreaKeywords = (): string =>
  AREAS.flatMap(a => a.keywords).join(', ');

/** 지역 이름 목록 (간략) */
export const getAreaNames = (): string[] =>
  AREAS.map(a => `${a.name}치과`);
